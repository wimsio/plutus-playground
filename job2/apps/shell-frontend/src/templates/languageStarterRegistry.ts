export function buildLanguageStarterTemplate(language: StarterLanguage, folder: string): StarterTemplateBundle {
  switch (language) {
    case "haskell":
      return buildHaskellStarter(folder);
    case "aiken":
      return buildAikenStarter(folder);
    case "opshin":
      return buildOpshinStarter(folder);
    case "helios":
      return buildHeliosStarter(folder);
    case "midnight":
      return buildMidnightStarter(folder);
    default:
      return buildHaskellStarter(folder);
  }
}

function capitalize(value: string) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}