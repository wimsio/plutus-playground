function offchainIndexTs(language: StarterLanguage) {
  return `export type ContractAction =
  | "lock"
  | "unlock"
  | "mint"
  | "burn"
  | "custom";

export type BuildTxInput = {
  action: ContractAction;
  walletAddress: string;
  amountLovelace?: number;
  metadata?: Record<string, unknown>;
};

export function build${capitalize(language)}TxPayload(input: BuildTxInput) {
  if (!input.walletAddress) {
    throw new Error("walletAddress is required");
  }

  return {
    stack: "${language}",
    action: input.action,
    walletAddress: input.walletAddress,
    amountLovelace: input.amountLovelace ?? 0,
    metadata: input.metadata ?? {},
  };
}
`;
}

function offchainTestTs(language: StarterLanguage) {
  return `import test from "node:test";
import assert from "node:assert/strict";
import { build${capitalize(language)}TxPayload } from "../src/index";

test("build${capitalize(language)}TxPayload builds a payload", () => {
  const payload = build${capitalize(language)}TxPayload({
    action: "lock",
    walletAddress: "addr_test1...",
    amountLovelace: 5000000,
  });

  assert.equal(payload.stack, "${language}");
  assert.equal(payload.action, "lock");
  assert.equal(payload.amountLovelace, 5000000);
});
`;
}

